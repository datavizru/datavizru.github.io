function buildTimeseries10x200() {
  const series = Array.from({ length: 10 }, (_, i) => `Ряд ${i + 1}`);
  const values = [];
  const start = new Date(2024, 0, 1);
  for (let d = 0; d < 200; d++) {
    const dt = new Date(start);
    dt.setDate(start.getDate() + d);
    const day = dt.toISOString().slice(0, 10);
    for (let s = 0; s < 10; s++) {
      const v =
        100 +
        s * 8 +
        d * 0.4 +
        Math.sin((d + s * 7) / 12) * 20 +
        ((d * 13 + s * 7) % 17) -
        8;
      values.push({ День: day, Серия: series[s], Значение: Math.round(v) });
    }
  }
  return values;
}

/** Детерминированный scatter (в приложении Math.random — здесь фиксированный набор). */
function buildScatter40() {
  let s = 12345;
  const rnd = () => {
    s = (s * 16807) % 2147483647;
    return (s - 1) / 2147483646;
  };
  return Array.from({ length: 40 }, () => {
    const x = +(rnd() * 100).toFixed(1);
    return { x, y: +(x * 0.6 + (rnd() * 40 - 20)).toFixed(1) };
  });
}

