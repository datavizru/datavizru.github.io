function escCell(v) {
  const s = String(v ?? "");
  return /[",\n\r]/.test(s) ? `"${s.replace(/"/g, '""')}"` : s;
}

function toCsv(rows) {
  if (!rows.length) return "";
  const cols = Object.keys(rows[0]);
  const lines = [cols.map(escCell).join(",")];
  for (const row of rows) lines.push(cols.map((c) => escCell(row[c])).join(","));
  return lines.join("\n") + "\n";
}

